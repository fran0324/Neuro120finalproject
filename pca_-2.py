# %% Experiment 2: Monte Carlo vector-field check for the matched rule
# This is the analytical intuition made numerical.
#
# For zero-mean Gaussian input and h = ReLU(w^T x),
#
#     E[h x]   = 0.5 Cw
#     E[h^2]   = 0.5 w^T C w
#
# so vanilla ReLU-Oja has the same expected vector-field direction as classic
# Oja, up to a factor of 0.5. With the accelerometer gate from the project, the
# expected update remains almost collinear with that Oja/PCA direction; the gate
# mainly changes the scalar speed of learning.
def relu_oja_expected_field(w, C, lambda_oja=1.0):
    s = float(w @ C @ w)
    return 0.5 * (C @ w - lambda_oja * s * w)


def monte_carlo_project_gated_field(
    w,
    eigvecs,
    eigvals,
    rng,
    n_mc_batches=8000,
    batch_size=64,
    alpha=0.90,
    g_max=2.0,
    lambda_oja=1.0,
):
    """Estimate E[g(t)(mean h x - lambda mean h^2 w)] for the project gate."""
    d = len(w)
    X_prev = sample_gaussian(n_mc_batches * batch_size, eigvecs, eigvals, rng)
    X = sample_gaussian(n_mc_batches * batch_size, eigvecs, eigvals, rng)
    X_prev = X_prev.reshape(n_mc_batches, batch_size, d)
    X = X.reshape(n_mc_batches, batch_size, d)

    h_prev = relu(X_prev @ w)
    h = relu(X @ w)
    m_prev = h_prev.mean(axis=1)
    m = h.mean(axis=1)

    # One-step gate with a(t-1)=0. This isolates whether the project gate
    # changes the direction of the expected local PCA update.
    r = np.maximum(0.0, m - m_prev)
    g = np.minimum(g_max, (1.0 - alpha) * r)

    hebb = (h[:, :, None] * X).mean(axis=1)
    post_second_moment = (h**2).mean(axis=1)
    updates = g[:, None] * (hebb - lambda_oja * post_second_moment[:, None] * w[None, :])
    return updates.mean(axis=0), float(g.mean())


def cosine(a, b, eps=1e-12):
    return float(a @ b / ((np.linalg.norm(a) + eps) * (np.linalg.norm(b) + eps)))


cosines, mean_gates = [], []
for _ in range(20):
    w = unit_norm(rng.normal(size=d))
    F_oja = relu_oja_expected_field(w, C, lambda_oja=lambda_oja)
    F_accel, mean_gate = monte_carlo_project_gated_field(
        w,
        eigvecs,
        eigvals,
        rng,
        n_mc_batches=8000,
        batch_size=batch_size,
        alpha=alpha,
        g_max=g_max,
        lambda_oja=lambda_oja,
    )
    cosines.append(cosine(F_oja, F_accel))
    mean_gates.append(mean_gate)

print(
    "Matched ReLU gate vector-field check: "
    f"mean cosine={np.mean(cosines):.4f}, "
    f"min cosine={np.min(cosines):.4f}, "
    f"mean one-step gate={np.mean(mean_gates):.5f}"
)

# %% Experiment 3: Fixed-input single-neuron dynamics
# If x_t = x* is fixed,
# then h(t) = ReLU(w_t^T x*) changes only because w_t changes during learning.
# Therefore r(t) = max(0, h(t)-h(t-1)) is literally a positive finite difference
# of the neuron's own activity over training time, matching the "activity
# derivative" intuition.
#
