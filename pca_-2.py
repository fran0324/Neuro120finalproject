# %% Basic helpers
def relu(x):
    return np.maximum(0.0, x)


def unit_norm(w, eps=1e-12):
    return w / (np.linalg.norm(w) + eps)


def moving_average(x, window=200):
    x = np.asarray(x)
    if window <= 1:
        return x
    kernel = np.ones(window) / window
    return np.convolve(x, kernel, mode="same")


def make_covariance(d=8, eigvals=None, seed=0):
    """Create a covariance matrix with known eigenvectors/eigenvalues."""
    local_rng = np.random.default_rng(seed)
    if eigvals is None:
        eigvals = np.array([4.0, 2.0, 1.2, 0.8, 0.55, 0.35, 0.25, 0.15])
    eigvals = np.asarray(eigvals, dtype=float)
    if len(eigvals) != d:
        raise ValueError("len(eigvals) must equal d")

    q, _ = np.linalg.qr(local_rng.normal(size=(d, d)))
    if np.linalg.det(q) < 0:
        q[:, 0] *= -1
    cov = q @ np.diag(eigvals) @ q.T
    return cov, q, eigvals


def sample_gaussian(n, eigvecs, eigvals, rng):
    """Sample N(0, C), where C = eigvecs diag(eigvals) eigvecs.T."""
    z = rng.normal(size=(n, len(eigvals)))
    return z @ (eigvecs * np.sqrt(eigvals)).T


def minibatches(X, batch_size):
    for start in range(0, len(X), batch_size):
        xb = X[start : start + batch_size]
        if len(xb) == batch_size:
            yield xb


@dataclass
class RunResult:
    name: str
    steps: np.ndarray
    align: np.ndarray
    variance: np.ndarray
    weight_norm: np.ndarray
    gates: np.ndarray
    update_norms: np.ndarray
    mean_activity: np.ndarray
    final_w: np.ndarray


def run_single_relu_oja(
    X,
    C,
    v1,
    w0,
    rule="oja",
    eta_hid=0.08,
    alpha=0.90,
    g_max=2.0,
    lambda_oja=1.0,
    batch_size=64,
    normalize_after_update=False,
    log_every=25,
):
    """Run the project-style one-neuron ReLU Hebbian-Oja rule.

    rule="oja":        g(t) = 1
    rule="accel":      g(t) from positive changes in mean ReLU activity
    rule="magnitude":  control gate from mean ReLU activity level

    normalize_after_update is False by default because the MLP hidden-layer
    update in the project uses the Oja stabilizer rather than explicit
    Euclidean normalization. Set it to True if you want the normalized
    single-neuron variant from the theorem in the write-up.
    """
    w = w0.copy()
    a = 0.0
    m_prev = 0.0

    n_batches = len(X) // batch_size
    gates = np.zeros(n_batches)
    update_norms = np.zeros(n_batches)
    mean_activity = np.zeros(n_batches)
    steps, align, variance, weight_norm = [], [], [], []

    for t, xb in enumerate(minibatches(X, batch_size), start=1):
        z = xb @ w
        h = relu(z)
        m = float(h.mean())

        hebb = (h[:, None] * xb).mean(axis=0)
        post_second_moment = float((h**2).mean())

        if rule == "oja":
            g = 1.0
        elif rule == "accel":
            r = max(0.0, m - m_prev)
            a = alpha * a + (1.0 - alpha) * r
            g = min(g_max, a)
        elif rule == "magnitude":
            a = alpha * a + (1.0 - alpha) * m
            g = min(g_max, a)
        else:
            raise ValueError(f"Unknown rule: {rule}")

        raw_update = eta_hid * g * (hebb - lambda_oja * post_second_moment * w)
        w = w + raw_update
        if normalize_after_update:
            w = unit_norm(w)

        gates[t - 1] = g
        update_norms[t - 1] = np.linalg.norm(raw_update)
        mean_activity[t - 1] = m
        m_prev = m

        if t == 1 or t % log_every == 0 or t == n_batches:
            norm_w = np.linalg.norm(w)
            steps.append(t)
            align.append(abs(float(w @ v1)) / (norm_w * np.linalg.norm(v1)))
            variance.append(float(unit_norm(w) @ C @ unit_norm(w)))
            weight_norm.append(norm_w)

    return RunResult(
        name=rule,
        steps=np.array(steps),
        align=np.array(align),
        variance=np.array(variance),
        weight_norm=np.array(weight_norm),
        gates=gates,
        update_norms=update_norms,
        mean_activity=mean_activity,
        final_w=w,
    )


def plot_learning_curves(results, title, savepath=None):
    fig, axes = plt.subplots(1, 3, figsize=(15, 4.2))
    for res in results:
        axes[0].plot(res.steps, res.align, label=res.name)
        axes[1].plot(res.steps, res.variance, label=res.name)
        axes[2].plot(res.steps, res.weight_norm, label=res.name)

    axes[0].set_title("Alignment with first PC")
    axes[0].set_xlabel("Minibatch step")
    axes[0].set_ylabel(r"$|w^T v_1| / ||w||$")
    axes[0].set_ylim(0, 1.02)

    axes[1].set_title("Variance captured")
    axes[1].set_xlabel("Minibatch step")
    axes[1].set_ylabel(r"$\hat{w}^T C \hat{w}$")

    axes[2].set_title("Weight norm")
    axes[2].set_xlabel("Minibatch step")
    axes[2].set_ylabel(r"$||w||$")

    fig.suptitle(title, y=1.04)
    axes[0].legend()
    plt.tight_layout()
    if savepath is not None:
        plt.savefig(savepath, dpi=300, bbox_inches="tight")
    plt.show()


def plot_gate_and_updates(results, title, smooth=100):
    fig, axes = plt.subplots(1, 3, figsize=(15, 4.2))
    for res in results:
        axes[0].plot(moving_average(res.mean_activity, smooth), label=res.name)
        axes[1].plot(moving_average(res.gates, smooth), label=res.name)
        axes[2].plot(moving_average(res.update_norms, smooth), label=res.name)

    axes[0].set_title("Mean ReLU activity")
    axes[0].set_xlabel("Minibatch step")
    axes[0].set_ylabel(r"$m(t)=mean_b ReLU(w^T x_b)$")

    axes[1].set_title("Gate")
    axes[1].set_xlabel("Minibatch step")
    axes[1].set_ylabel(r"$g(t)$")

    axes[2].set_title("Raw update norm")
    axes[2].set_xlabel("Minibatch step")
    axes[2].set_ylabel(r"$||\Delta w||$")

    fig.suptitle(title, y=1.04)
    axes[0].legend()
    plt.tight_layout()
    plt.show()


def centered_gram(y):
    k = np.outer(y, y)
    return k - k.mean(axis=0, keepdims=True) - k.mean(axis=1, keepdims=True) + k.mean()


def upper_tri_corr(A, B):
    idx = np.triu_indices_from(A, k=1)
    a = A[idx] - A[idx].mean()
    b = B[idx] - B[idx].mean()
    denom = np.linalg.norm(a) * np.linalg.norm(b)
    return float(a @ b / denom) if denom > 0 else np.nan


def relu_similarity_matching_score(X_eval, w, v1):
    """Compare learned ReLU output similarity to the ideal first-PC ReLU code.

    Because PCA direction is sign-invariant, compare against both +v1 and -v1.
    """
    h_learned = relu(X_eval @ w)
    score_pos = upper_tri_corr(centered_gram(h_learned), centered_gram(relu(X_eval @ v1)))
    score_neg = upper_tri_corr(centered_gram(h_learned), centered_gram(relu(X_eval @ (-v1))))
    return max(score_pos, score_neg)

