from scipy.stats import spearmanr
from scipy.spatial.distance import pdist, squareform
import copy


def upper_tri_vec(M: np.ndarray) -> np.ndarray:
    idx = np.triu_indices_from(M, k=1)
    return M[idx]


def cosine_similarity_vec(a: np.ndarray, b: np.ndarray, eps: float = 1e-12) -> float:
    na = np.linalg.norm(a)
    nb = np.linalg.norm(b)
    if na < eps or nb < eps:
        return np.nan
    return float(np.dot(a, b) / (na * nb))


def average_patterns_by_label(X: np.ndarray, y: np.ndarray):
    classes = np.unique(y)
    X_avg = []
    kept = []
    for c in classes:
        mask = (y == c)
        if mask.sum() > 0:
            X_avg.append(X[mask].mean(axis=0))
            kept.append(int(c))
    return np.stack(X_avg, axis=0), np.array(kept, dtype=int)


def rdm_from_patterns(X: np.ndarray, metric: str = "correlation") -> np.ndarray:
    # X: [n_conditions, n_features]
    return squareform(pdist(X, metric=metric))


def rsa_between_rdms(brain_rdm: np.ndarray, model_rdm: np.ndarray):
    b = upper_tri_vec(brain_rdm)
    m = upper_tri_vec(model_rdm)

    spear = spearmanr(b, m).statistic
    cos = cosine_similarity_vec(b, m)

    return {
        "rsa_spearman": float(spear) if np.isfinite(spear) else np.nan,
        "rsa_cosine": float(cos) if np.isfinite(cos) else np.nan,
    }


@torch.no_grad()
def extract_hidden_features(model, X: np.ndarray, batch_size: int = 256, device: torch.device = device):
    model.eval()
    feats = []
    n = X.shape[0]
    for start in range(0, n, batch_size):
        xb = torch.tensor(X[start:start + batch_size], dtype=torch.float32, device=device)
        _, hidden = model(xb)
        feats.append(hidden.detach().cpu().numpy())
    return np.concatenate(feats, axis=0)

@torch.no_grad()
def evaluate_array_model(model, loader, device):
    model.eval()
    total = 0
    correct = 0
    total_loss = 0.0

    for xb, yb in loader:
        xb = xb.to(device)
        yb = yb.to(device)

        logits, _ = model(xb)
        loss = F.cross_entropy(logits, yb, reduction="sum")

        total_loss += float(loss.item())
        correct += int((logits.argmax(dim=1) == yb).sum().item())
        total += yb.size(0)

    return total_loss / max(total, 1), correct / max(total, 1)

def train_one_array_run_return_model(
    X_train: np.ndarray,
    y_train: np.ndarray,
    X_test: np.ndarray,
    y_test: np.ndarray,
    rule: str,
    seed: int,
    device: torch.device,
    hidden_dim: int = 256,
    epochs: int = 30,
    batch_size: int = 64,
    lr: float = 0.05,
    momentum: float = 0.9,
    lr_newton: float = 0.1,
    base_damping: float = 10.0,
    max_cg_iter: int = 6,
    cg_tol: float = 1e-3,
    alpha_gate: float = 0.9,
    g_max: float = 1.0,
    kappa: float = 1.0,
):
    set_seed(seed)

    input_dim = X_train.shape[1]
    num_classes = int(max(y_train.max(), y_test.max()) + 1)

    # SmallMLP in the notebook uses output_dim, not num_classes
    model = SmallMLP(
        input_dim=input_dim,
        hidden_dim=hidden_dim,
        output_dim=num_classes,
    ).to(device)

    train_loader, test_loader = make_array_loaders(
        X_train, y_train, X_test, y_test, batch_size=batch_size
    )

    if rule == "sgd":
        optimizer = torch.optim.SGD(model.parameters(), lr=lr, momentum=0.0)
    elif rule == "momentum":
        optimizer = torch.optim.SGD(model.parameters(), lr=lr, momentum=momentum)
    else:
        optimizer = None

    gate_state = {
        "prev_hidden_mean": None,
        "gate_trace": None,
    }

    best_test_acc = 0.0
    last_info = None

    for epoch in range(epochs):
        model.train()

        for xb, yb in train_loader:
            xb = xb.to(device)
            yb = yb.to(device)

            if rule in {"sgd", "momentum"}:
                optimizer.zero_grad(set_to_none=True)
                logits, hidden = model(xb)
                loss = F.cross_entropy(logits, yb)
                loss.backward()
                optimizer.step()

                with torch.no_grad():
                    batch_acc = float((logits.argmax(dim=1) == yb).float().mean().item())
                    grad_sq = 0.0
                    for p in model.parameters():
                        if p.grad is not None:
                            grad_sq += float((p.grad.detach() ** 2).sum().item())
                    grad_norm = math.sqrt(max(grad_sq, 0.0))
                    param_norm = float(parameters_to_vector([p.detach() for p in model.parameters()]).norm().item())

                last_info = {
                    "loss": float(loss.item()),
                    "acc": batch_acc,
                    "grad_norm": grad_norm,
                    "update_norm": np.nan,
                    "param_norm": param_norm,
                    "damping_mean": np.nan,
                    "damping_min": np.nan,
                    "damping_max": np.nan,
                    "hidden": hidden.detach(),
                    "cg_status": "first_order",
                    "cg_iters": 0,
                    "step_scale": 1.0,
                }

            elif rule in {"newton", "gated_newton"}:
                with torch.no_grad():
                    xb_flat = prepare_mlp_input(xb)
                    _, hidden0 = model(xb_flat)
                    hidden_mean = hidden0.mean(dim=0)

                    if gate_state["prev_hidden_mean"] is None:
                        gate_state["prev_hidden_mean"] = hidden_mean.clone()
                        gate_state["gate_trace"] = torch.zeros_like(hidden_mean)

                    delta = hidden_mean - gate_state["prev_hidden_mean"]
                    pos_delta = torch.relu(delta)
                    gate_state["gate_trace"] = alpha_gate * gate_state["gate_trace"] + (1.0 - alpha_gate) * pos_delta
                    hidden_gate = torch.clamp(gate_state["gate_trace"], 0.0, g_max)
                    gate_state["prev_hidden_mean"] = hidden_mean.clone()

                hidden_gate_for_step = None if rule == "newton" else hidden_gate

                last_info = newton_cg_step(
                    model=model,
                    xb=xb,
                    yb=yb,
                    lr=lr_newton,
                    base_damping=base_damping,
                    max_cg_iter=max_cg_iter,
                    cg_tol=cg_tol,
                    hidden_gate=hidden_gate_for_step,
                    kappa=kappa,
                )

            else:
                raise ValueError(f"Unknown rule: {rule}")

        test_loss, test_acc = evaluate_array_model(model, test_loader, device)
        best_test_acc = max(best_test_acc, test_acc)

    train_loss, train_acc = evaluate_array_model(model, train_loader, device)
    test_loss, test_acc = evaluate_array_model(model, test_loader, device)

    metrics = {
        "dataset": "haxby",
        "rule": rule,
        "seed": seed,
        "final_test_accuracy": float(test_acc),
        "best_test_accuracy": float(best_test_acc),
        "train_accuracy": float(train_acc),
        "final_train_loss": float(train_loss),
        "final_test_loss": float(test_loss),
    }

    if last_info is not None:
        metrics.update({
            "last_grad_norm": float(last_info["grad_norm"]) if np.isfinite(last_info["grad_norm"]) else np.nan,
            "last_update_norm": float(last_info["update_norm"]) if np.isfinite(last_info["update_norm"]) else np.nan,
            "last_param_norm": float(last_info["param_norm"]) if np.isfinite(last_info["param_norm"]) else np.nan,
            "last_damping_mean": float(last_info["damping_mean"]) if np.isfinite(last_info["damping_mean"]) else np.nan,
            "last_damping_min": float(last_info["damping_min"]) if np.isfinite(last_info["damping_min"]) else np.nan,
            "last_damping_max": float(last_info["damping_max"]) if np.isfinite(last_info["damping_max"]) else np.nan,
            "last_cg_status": last_info["cg_status"],
            "last_cg_iters": int(last_info["cg_iters"]),
            "last_step_scale": float(last_info["step_scale"]),
        })

    return metrics, copy.deepcopy(model).cpu()

def run_haxby_cv_with_rsa(
    X: np.ndarray,
    y: np.ndarray,
    groups: np.ndarray,
    rules: list[str],
    seeds: list[int],
    n_splits: int = 5,
    hidden_dim: int = 256,
    epochs: int = 30,
    batch_size: int = 64,
    lr: float = 0.05,
    momentum: float = 0.9,
    lr_newton: float = 0.1,
    base_damping: float = 10.0,
    max_cg_iter: int = 6,
    cg_tol: float = 1e-3,
    alpha_gate: float = 0.9,
    g_max: float = 1.0,
    kappa: float = 1.0,
    pca_components: int = 128,
):
    splitter = GroupKFold(n_splits=n_splits)
    rows = []

    for fold_idx, (train_idx, test_idx) in enumerate(splitter.split(X, y, groups=groups), start=1):
        X_train_raw = X[train_idx]
        X_test_raw = X[test_idx]
        y_train = y[train_idx]
        y_test = y[test_idx]

        scaler = StandardScaler()
        X_train_scaled = scaler.fit_transform(X_train_raw)
        X_test_scaled = scaler.transform(X_test_raw)

        n_comp = min(pca_components, X_train_scaled.shape[0] - 1, X_train_scaled.shape[1])
        if n_comp >= 2:
            pca = PCA(n_components=n_comp, random_state=0)
            X_train = pca.fit_transform(X_train_scaled).astype(np.float32)
            X_test = pca.transform(X_test_scaled).astype(np.float32)
        else:
            pca = None
            X_train = X_train_scaled.astype(np.float32)
            X_test = X_test_scaled.astype(np.float32)

        # Brain-side RDM on held-out test fold, using raw voxel patterns averaged by class
        Xb_avg, kept_classes_brain = average_patterns_by_label(X_test_raw, y_test)
        brain_rdm = rdm_from_patterns(Xb_avg, metric="correlation")

        print(f"\nFold {fold_idx}: train={len(train_idx)}, test={len(test_idx)}, input_dim={X_train.shape[1]}")

        for rule in rules:
            for seed in seeds:
                metrics, model_cpu = train_one_array_run_return_model(
                    X_train=X_train,
                    y_train=y_train,
                    X_test=X_test,
                    y_test=y_test,
                    rule=rule,
                    seed=seed,
                    device=device,
                    hidden_dim=hidden_dim,
                    epochs=epochs,
                    batch_size=batch_size,
                    lr=lr,
                    momentum=momentum,
                    lr_newton=lr_newton,
                    base_damping=base_damping,
                    max_cg_iter=max_cg_iter,
                    cg_tol=cg_tol,
                    alpha_gate=alpha_gate,
                    g_max=g_max,
                    kappa=kappa,
                )

                # Model-side RDM from hidden activations on the same held-out stimuli
                hidden_test = extract_hidden_features(model_cpu.to(device), X_test, batch_size=batch_size, device=device)
                Xm_avg, kept_classes_model = average_patterns_by_label(hidden_test, y_test)

                # Align class order explicitly
                common = np.intersect1d(kept_classes_brain, kept_classes_model)
                if len(common) < 3:
                    rsa_stats = {"rsa_spearman": np.nan, "rsa_cosine": np.nan}
                else:
                    Xb_common = np.stack([Xb_avg[np.where(kept_classes_brain == c)[0][0]] for c in common], axis=0)
                    Xm_common = np.stack([Xm_avg[np.where(kept_classes_model == c)[0][0]] for c in common], axis=0)

                    brain_rdm_common = rdm_from_patterns(Xb_common, metric="correlation")
                    model_rdm_common = rdm_from_patterns(Xm_common, metric="correlation")
                    rsa_stats = rsa_between_rdms(brain_rdm_common, model_rdm_common)

                row = dict(metrics)
                row.update({
                    "fold": fold_idx,
                    "n_train": len(train_idx),
                    "n_test": len(test_idx),
                    "rsa_spearman": rsa_stats["rsa_spearman"],
                    "rsa_cosine": rsa_stats["rsa_cosine"],
                })
                rows.append(row)

                print(
                    f"rule={rule:13s} seed={seed} "
                    f"acc={row['final_test_accuracy']:.4f} "
                    f"rsa_s={row['rsa_spearman']:.4f} "
                    f"rsa_c={row['rsa_cosine']:.4f}"
                )

    return pd.DataFrame(rows)

