"""
This cell defines the proposed accelerometer gate and turns it into a **unitwise damping vector** for the gated Newton-CG method."""

@torch.no_grad()
def update_gate_from_hidden(
    hidden: torch.Tensor,
    state: GateState,
    alpha: float,
    g_max: float,
):
    current_mean = hidden.mean(dim=0)
    delta = current_mean - state.prev_mean
    rise = torch.relu(delta)
    state.trace = alpha * state.trace + (1.0 - alpha) * rise
    gate = torch.clamp(state.trace, 0.0, g_max)
    state.prev_mean = current_mean.detach()

    sat = float((gate >= g_max - 1e-8).float().mean().item())
    return gate.detach(), float(gate.mean().item()), sat


def make_hidden_gate_state(hidden_dim: int, device: torch.device):
    return GateState(
        prev_mean=torch.zeros(hidden_dim, device=device),
        trace=torch.zeros(hidden_dim, device=device),
    )


def make_damping_vector(
    model: SmallMLP,
    base_damping: float,
    hidden_gate: torch.Tensor | None = None,
    kappa: float = 1.0,
):
    # fc1.weight: [hidden_dim, input_dim]
    # fc1.bias:   [hidden_dim]
    # fc2.weight / fc2.bias: use base damping
    hidden_dim, input_dim = model.fc1.weight.shape

    if hidden_gate is None:
        hidden_lambdas = torch.full((hidden_dim,), base_damping, device=model.fc1.weight.device)
    else:
        hidden_lambdas = base_damping / (1.0 + kappa * hidden_gate)

    v1 = hidden_lambdas.repeat_interleave(input_dim)
    v2 = hidden_lambdas
    v3 = torch.full_like(model.fc2.weight.data.reshape(-1), base_damping)
    v4 = torch.full_like(model.fc2.bias.data.reshape(-1), base_damping)

    damping_vec = torch.cat([v1, v2, v3, v4], dim=0)
    stats = {
        "mean": float(damping_vec.mean().item()),
        "min": float(damping_vec.min().item()),
        "max": float(damping_vec.max().item()),
    }
    return damping_vec, stats

"""
This cell implements a truncated conjugate-gradient solve for the damped Newton system using Hessian-vector products from autograd. The Hessian is never formed explicitly."""

def _params(model):
    return [p for p in model.parameters() if p.requires_grad]


def _vectorize_params(params):
    return parameters_to_vector([p.detach().clone() for p in params])


def _assign_vector_to_params(vec, params):
    offset = 0
    for p in params:
        numel = p.numel()
        p.copy_(vec[offset:offset + numel].view_as(p))
        offset += numel


def conjugate_gradients_safe(Av, b, max_iter: int = 10, tol: float = 1e-4, eps: float = 1e-10):
    x = torch.zeros_like(b)
    r = b - Av(x)
    p = r.clone()
    rs_old = torch.dot(r, r)

    if not torch.isfinite(rs_old):
        return x, {"status": "nonfinite_init", "iters": 0}

    for k in range(max_iter):
        Ap = Av(p)

        if not torch.isfinite(Ap).all():
            return x, {"status": "nonfinite_Ap", "iters": k}

        denom = torch.dot(p, Ap)

        # CG needs positive curvature; bail out if curvature is bad or nearly singular
        if (not torch.isfinite(denom)) or (denom <= eps):
            return x, {"status": "bad_curvature", "iters": k}

        alpha = rs_old / denom

        if not torch.isfinite(alpha):
            return x, {"status": "nonfinite_alpha", "iters": k}

        x = x + alpha * p
        r = r - alpha * Ap
        rs_new = torch.dot(r, r)

        if not torch.isfinite(rs_new):
            return x, {"status": "nonfinite_residual", "iters": k + 1}

        if torch.sqrt(rs_new) < tol:
            return x, {"status": "converged", "iters": k + 1}

        beta = rs_new / rs_old.clamp_min(eps)

        if not torch.isfinite(beta):
            return x, {"status": "nonfinite_beta", "iters": k + 1}

        p = r + beta * p
        rs_old = rs_new

    return x, {"status": "max_iter", "iters": max_iter}


def hvp_from_loss(loss, params, vec):
    grads = torch.autograd.grad(loss, params, create_graph=True, retain_graph=True)
    gvec = parameters_to_vector([g.contiguous() for g in grads])
    dot = torch.dot(gvec, vec)
    hv_parts = torch.autograd.grad(dot, params, retain_graph=True)
    hv = parameters_to_vector([h.contiguous() for h in hv_parts])
    return gvec, hv


def newton_cg_step(
    model: SmallMLP,
    xb: torch.Tensor,
    yb: torch.Tensor,
    lr: float,
    base_damping: float,
    max_cg_iter: int,
    cg_tol: float,
    hidden_gate: torch.Tensor | None = None,
    kappa: float = 1.0,
):
    model.zero_grad(set_to_none=True)

    xb = prepare_mlp_input(xb)

    logits, hidden = model(xb)
    loss = F.cross_entropy(logits, yb)

    params = _params(model)

    damping_vec, damping_stats = make_damping_vector(
        model=model,
        base_damping=base_damping,
        hidden_gate=hidden_gate,
        kappa=kappa,
    )

    zero_vec = torch.zeros_like(_vectorize_params(params))
    gvec, _ = hvp_from_loss(loss, params, zero_vec)
    gvec = gvec.detach()

    if (not torch.isfinite(gvec).all()) or (not torch.isfinite(loss)):
        return {
            "loss": float("nan"),
            "acc": float("nan"),
            "grad_norm": float("nan"),
            "update_norm": float("nan"),
            "param_norm": float("nan"),
            "damping_mean": damping_stats["mean"],
            "damping_min": damping_stats["min"],
            "damping_max": damping_stats["max"],
            "hidden": hidden.detach(),
            "cg_status": "nonfinite_grad",
            "cg_iters": 0,
            "step_scale": 0.0,
        }

    grad_norm = float(gvec.norm().item())

    def Av(v):
        _, hv = hvp_from_loss(loss, params, v)
        out = hv.detach() + damping_vec.detach() * v
        if not torch.isfinite(out).all():
            raise RuntimeError("Nonfinite HVP in Av")
        return out

    try:
        p, cg_info = conjugate_gradients_safe(
            Av=Av,
            b=gvec,
            max_iter=max_cg_iter,
            tol=cg_tol,
        )
    except RuntimeError:
        p = gvec.clone()
        cg_info = {"status": "runtime_error_fallback_grad", "iters": 0}

    if (not torch.isfinite(p).all()) or cg_info["status"] not in {"converged", "max_iter"}:
        p = gvec.clone()
        cg_info["status"] = f"fallback_grad_{cg_info['status']}"

    theta0 = _vectorize_params(params)
    base_loss = float(loss.item())

    step_scale = lr
    accepted = False
    update = None

    with torch.no_grad():
        for _ in range(8):
            update = step_scale * p
            theta_trial = theta0 - update
            _assign_vector_to_params(theta_trial, params)

            logits_trial, _ = model(xb)
            loss_trial = F.cross_entropy(logits_trial, yb)

            if torch.isfinite(loss_trial) and float(loss_trial.item()) <= base_loss:
                accepted = True
                break

            step_scale *= 0.5

        if not accepted:
            _assign_vector_to_params(theta0, params)
            step_scale = min(1e-3, lr * 0.01)
            update = step_scale * gvec
            theta_trial = theta0 - update
            _assign_vector_to_params(theta_trial, params)
            cg_info["status"] = f"{cg_info['status']}_tiny_grad_fallback"

    with torch.no_grad():
        logits_after, _ = model(xb)
        batch_acc = float((logits_after.argmax(dim=1) == yb).float().mean().item())
        param_norm = float(_vectorize_params(params).norm().item())
        update_norm = float(update.norm().item()) if update is not None else 0.0

    return {
        "loss": float(loss.item()),
        "acc": batch_acc,
        "grad_norm": grad_norm,
        "update_norm": update_norm,
        "param_norm": param_norm,
        "damping_mean": damping_stats["mean"],
        "damping_min": damping_stats["min"],
        "damping_max": damping_stats["max"],
        "hidden": hidden.detach(),
        "cg_status": cg_info["status"],
        "cg_iters": cg_info["iters"],
        "step_scale": step_scale,
    }

"""
This cell defines the shared SGD / SGD-with-momentum step, so that all four methods can be run through a common training driver."""

def first_order_step(
    model: SmallMLP,
    xb: torch.Tensor,
    yb: torch.Tensor,
    optimizer: torch.optim.Optimizer,
):
    model.zero_grad(set_to_none=True)
    logits, hidden = model(xb)
    loss = F.cross_entropy(logits, yb)
    loss.backward()

    grad_vec = parameters_to_vector([p.grad.detach() for p in model.parameters() if p.grad is not None])
    grad_norm = float(grad_vec.norm().item())

    theta_before = parameters_to_vector([p.detach().clone() for p in model.parameters()])
    optimizer.step()
    theta_after = parameters_to_vector([p.detach() for p in model.parameters()])
    update_norm = float((theta_after - theta_before).norm().item())

    with torch.no_grad():
        logits_after, _ = model(xb)
        batch_acc = float((logits_after.argmax(dim=1) == yb).float().mean().item())

    return {
        "loss": float(loss.item()),
        "acc": batch_acc,
        "grad_norm": grad_norm,
        "update_norm": update_norm,
        "param_norm": float(theta_after.norm().item()),
        "damping_mean": None,
        "damping_min": None,
        "damping_max": None,
        "hidden": hidden.detach(),
    }

"""
This cell runs one full training experiment for any of the four methods and stores both stepwise diagnostics and a run summary."""

@torch.no_grad()
def evaluate_model(model: SmallMLP, loader: DataLoader, device: torch.device):
    model.eval()
    total = 0
    total_loss = 0.0
    correct = 0
    for xb, yb in loader:
        xb = xb.to(device)
        yb = yb.to(device)
        if xb.ndim > 2:
            xb = xb.view(xb.size(0), -1)
        logits, _ = model(xb)
        loss = F.cross_entropy(logits, yb, reduction="sum")
        total_loss += float(loss.item())
        correct += int((logits.argmax(dim=1) == yb).sum().item())
        total += yb.size(0)
    return total_loss / max(total, 1), correct / max(total, 1)


def train_one_run(
    task_name: str,
    method: str,
    train_loader: DataLoader,
    test_loader: DataLoader,
    input_dim: int,
    output_dim: int,
    seed: int = 0,
    hidden_dim: int = 256,
    epochs: int = 10,
    lr: float = 0.05,
    momentum: float = 0.9,
    base_damping: float = 1.0,
    alpha: float = 0.9,
    g_max: float = 1.0,
    kappa: float = 2.0,
    max_cg_iter: int = 10,
    cg_tol: float = 1e-4,
):
    set_seed(seed)
    model = SmallMLP(input_dim=input_dim, hidden_dim=hidden_dim, output_dim=output_dim).to(device)

    if method == "sgd":
        optimizer = torch.optim.SGD(model.parameters(), lr=lr, momentum=0.0)
    elif method == "sgd_momentum":
        optimizer = torch.optim.SGD(model.parameters(), lr=lr, momentum=momentum)
    else:
        optimizer = None

    gate_state = make_hidden_gate_state(hidden_dim=hidden_dim, device=device)
    step_records = []
    unstable = False
    t0 = time.time()
    global_step = 0
    best_test_acc = 0.0

    for epoch in range(1, epochs + 1):
        model.train()
        for xb, yb in train_loader:
            xb = xb.to(device)
            yb = yb.to(device)
            if xb.ndim > 2:
                xb = xb.view(xb.size(0), -1)

            global_step += 1
            gate_mean = None
            gate_sat = None

            try:
                if method in {"sgd", "sgd_momentum"}:
                    out = first_order_step(model, xb, yb, optimizer)
                elif method == "damped_newton_cg":
                    out = newton_cg_step(
                        model=model,
                        xb=xb,
                        yb=yb,
                        lr=lr,
                        base_damping=base_damping,
                        max_cg_iter=max_cg_iter,
                        cg_tol=cg_tol,
                        hidden_gate=None,
                        kappa=kappa,
                    )
                elif method == "gated_damped_newton_cg":
                    with torch.no_grad():
                        _, hidden_pre = model(xb)
                        hidden_gate, gate_mean, gate_sat = update_gate_from_hidden(
                            hidden=hidden_pre,
                            state=gate_state,
                            alpha=alpha,
                            g_max=g_max,
                        )

                    out = newton_cg_step(
                        model=model,
                        xb=xb,
                        yb=yb,
                        lr=lr,
                        base_damping=base_damping,
                        max_cg_iter=max_cg_iter,
                        cg_tol=cg_tol,
                        hidden_gate=hidden_gate,
                        kappa=kappa,
                    )
                else:
                    raise ValueError(f"Unknown method: {method}")
            except RuntimeError as e:
                print(f"RuntimeError in {method}: {e}")
                unstable = True
                break

            if not np.isfinite(out["loss"]) or not np.isfinite(out["grad_norm"]) or not np.isfinite(out["update_norm"]):
                unstable = True
                break

            step_records.append(
                StepRecord(
                    epoch=epoch,
                    step=global_step,
                    wall_time=time.time() - t0,
                    train_loss=out["loss"],
                    train_acc=out["acc"],
                    grad_norm=out["grad_norm"],
                    update_norm=out["update_norm"],
                    param_norm=out["param_norm"],
                    gate_mean=gate_mean,
                    gate_sat=gate_sat,
                    damping_mean=out["damping_mean"],
                    damping_min=out["damping_min"],
                    damping_max=out["damping_max"],
                )
            )

        if unstable:
            break

        test_loss, test_acc = evaluate_model(model, test_loader, device)
        best_test_acc = max(best_test_acc, test_acc)
        print(
            f"{task_name} | {method} | seed={seed} | epoch={epoch} | "
            f"test_loss={test_loss:.4f} | test_acc={test_acc:.4f}"
        )

    total_wall = time.time() - t0
    test_loss, test_acc = evaluate_model(model, test_loader, device)

    summary = RunSummary(
        task=task_name,
        method=method,
        seed=seed,
        final_test_loss=test_loss,
        final_test_acc=test_acc,
        best_test_acc=best_test_acc,
        total_wall_time=total_wall,
        unstable=unstable,
        hidden_weight_norm=float(model.fc1.weight.data.norm().item()),
        output_weight_norm=float(model.fc2.weight.data.norm().item()),
    )

    return model, pd.DataFrame([asdict(r) for r in step_records]), summary

