def make_cifar100_loaders(
    batch_size: int = 256,
    train_subset: int | None = None,
    test_subset: int | None = None,
    root: str = "./data",
):
    transform = transforms.Compose([
        transforms.ToTensor(),
        transforms.Normalize(
            mean=(0.5071, 0.4867, 0.4408),
            std=(0.2675, 0.2565, 0.2761),
        ),
    ])

    train_ds = datasets.CIFAR100(root=root, train=True, download=True, transform=transform)
    test_ds = datasets.CIFAR100(root=root, train=False, download=True, transform=transform)

    if train_subset is not None:
        train_ds = Subset(train_ds, list(range(min(train_subset, len(train_ds)))))
    if test_subset is not None:
        test_ds = Subset(test_ds, list(range(min(test_subset, len(test_ds)))))

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=2, pin_memory=True)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False, num_workers=2, pin_memory=True)

    input_dim = 32 * 32 * 3
    num_classes = 100
    return train_loader, test_loader, input_dim, num_classes


def load_haxby_arrays(
    subject_id: int = 2,
    use_vt_mask: bool = True,
    keep_scrambledpix: bool = False,
    pca_components: int | None = 128,
):
    haxby = nilearn_datasets.fetch_haxby(subjects=[subject_id])
    session_df = pd.read_csv(haxby.session_target[0], sep=r"\s+")

    label_col = "labels" if "labels" in session_df.columns else "condition"
    group_col = "chunks" if "chunks" in session_df.columns else "chunk"

    if use_vt_mask and hasattr(haxby, "mask_vt"):
        mask_img = haxby.mask_vt[0] if isinstance(haxby.mask_vt, (list, tuple)) else haxby.mask_vt
    else:
        mask_img = haxby.mask[0] if isinstance(haxby.mask, (list, tuple)) else haxby.mask

    masker = NiftiMasker(mask_img=mask_img, standardize=True, detrend=False)
    X_full = masker.fit_transform(haxby.func[0])

    keep = session_df[label_col].astype(str) != "rest"
    if not keep_scrambledpix:
        keep = keep & (session_df[label_col].astype(str) != "scrambledpix")

    X = X_full[keep.values]
    y_text = session_df.loc[keep, label_col].astype(str).to_numpy()
    groups = session_df.loc[keep, group_col].to_numpy()

    encoder = LabelEncoder()
    y = encoder.fit_transform(y_text)

    scaler = StandardScaler()
    X = scaler.fit_transform(X)

    if pca_components is not None:
        n_comp = min(pca_components, X.shape[0] - 1, X.shape[1])
        if n_comp >= 2:
            pca = PCA(n_components=n_comp, random_state=0)
            X = pca.fit_transform(X)

    return X.astype(np.float32), y.astype(np.int64), groups, encoder


