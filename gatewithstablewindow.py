def set_seed(seed: int):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


def make_ramp(T, start, width, max_amp=1.0):
    r = np.zeros(T, dtype=np.float32)
    end = min(T, start + width)
    if end > start:
        r[start:end] = np.linspace(0.0, max_amp, end - start, endpoint=False, dtype=np.float32)
    if end < T:
        r[end:] = max_amp
    return r


def make_false_start(T, start, width, amp):
    r = np.zeros(T, dtype=np.float32)
    end = min(T, start + width)
    if end > start:
        r[start:end] = np.linspace(0.0, amp, end - start, endpoint=False, dtype=np.float32)
    return r


def generate_order_of_onset_dataset(
    n_samples=4000,
    T=30,
    frame_dim=20,
    noise_std=0.18,
    onset_gap=6,
    ramp_width=4,
    jitter=1,
    false_start_prob=0.15,
    false_start_amp=0.08,
    false_start_width=2,
    tail_length=8,
    overlap=0.55,
    seed=0,
):
    rng = np.random.default_rng(seed)

    uA = rng.normal(size=frame_dim).astype(np.float32)
    uB = rng.normal(size=frame_dim).astype(np.float32)
    uB = overlap * uA + (1.0 - overlap) * uB

    uA /= np.linalg.norm(uA) + 1e-8
    uB /= np.linalg.norm(uB) + 1e-8

    X = np.zeros((n_samples, T, frame_dim), dtype=np.float32)
    y = np.zeros(n_samples, dtype=np.int64)

    latest_start = T - ramp_width - max(tail_length, 2) - 1
    latest_start = max(latest_start, 5)

    for i in range(n_samples):
        cls = rng.integers(0, 2)
        y[i] = cls

        base_start = rng.integers(5, latest_start + 1)

        if cls == 0:
            start_A = base_start
            start_B = base_start + onset_gap + rng.integers(-jitter, jitter + 1)
        else:
            start_B = base_start
            start_A = base_start + onset_gap + rng.integers(-jitter, jitter + 1)

        start_A = max(1, min(T - ramp_width - tail_length - 1, start_A))
        start_B = max(1, min(T - ramp_width - tail_length - 1, start_B))

        rA = make_ramp(T, start_A, ramp_width, max_amp=1.0)
        rB = make_ramp(T, start_B, ramp_width, max_amp=1.0)

        if tail_length > 0:
            tail_start = max(0, T - tail_length)
            rA[tail_start:] = 1.0
            rB[tail_start:] = 1.0

        if rng.random() < false_start_prob:
            fs_max = max(2, min(start_A, start_B))
            fs_start = rng.integers(1, fs_max)
            if cls == 0:
                rB += make_false_start(T, fs_start, false_start_width, false_start_amp)
            else:
                rA += make_false_start(T, fs_start, false_start_width, false_start_amp)

        seq = np.outer(rA, uA) + np.outer(rB, uB)
        seq += rng.normal(scale=noise_std, size=seq.shape).astype(np.float32)
        X[i] = seq

    return X, y, uA, uB


def build_loaders(
    tail_length,
    n_train=3000,
    n_test=1000,
    batch_size=128,
    seed=42,
    T=30,
    frame_dim=20,
    noise_std=0.18,
    onset_gap=6,
    ramp_width=4,
    jitter=1,
    false_start_prob=0.15,
    false_start_amp=0.08,
    false_start_width=2,
    overlap=0.55,
):
    X_train, y_train, uA, uB = generate_order_of_onset_dataset(
        n_samples=n_train, T=T, frame_dim=frame_dim, noise_std=noise_std,
        onset_gap=onset_gap, ramp_width=ramp_width, jitter=jitter,
        false_start_prob=false_start_prob, false_start_amp=false_start_amp,
        false_start_width=false_start_width, tail_length=tail_length,
        overlap=overlap, seed=seed
    )
    X_test, y_test, _, _ = generate_order_of_onset_dataset(
        n_samples=n_test, T=T, frame_dim=frame_dim, noise_std=noise_std,
        onset_gap=onset_gap, ramp_width=ramp_width, jitter=jitter,
        false_start_prob=false_start_prob, false_start_amp=false_start_amp,
        false_start_width=false_start_width, tail_length=tail_length,
        overlap=overlap, seed=seed + 1
    )

    X_train_flat = X_train.reshape(n_train, T * frame_dim)
    X_test_flat = X_test.reshape(n_test, T * frame_dim)

    scaler = StandardScaler()
    X_train_flat = scaler.fit_transform(X_train_flat).astype(np.float32)
    X_test_flat = scaler.transform(X_test_flat).astype(np.float32)

    train_ds = TensorDataset(
        torch.tensor(X_train_flat, dtype=torch.float32),
        torch.tensor(y_train, dtype=torch.long),
    )
    test_ds = TensorDataset(
        torch.tensor(X_test_flat, dtype=torch.float32),
        torch.tensor(y_test, dtype=torch.long),
    )

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False)

    meta = {
        "T": T,
        "frame_dim": frame_dim,
        "input_dim": T * frame_dim,
        "num_classes": 2,
        "tail_length": tail_length,
        "X_train_seq": X_train,
        "X_test_seq": X_test,
        "y_train": y_train,
        "y_test": y_test,
        "uA": uA,
        "uB": uB,
    }
    return train_loader, test_loader, meta


