class OneHiddenMLP(nn.Module):
    def __init__(self, input_dim, hidden_dim, output_dim):
        super().__init__()
        self.fc1 = nn.Linear(input_dim, hidden_dim, bias=False)
        self.fc2 = nn.Linear(hidden_dim, output_dim)
        nn.init.normal_(self.fc1.weight, mean=0.0, std=0.05)
        nn.init.normal_(self.fc2.weight, mean=0.0, std=0.05)
        nn.init.zeros_(self.fc2.bias)

    def forward(self, x):
        h = F.relu(self.fc1(x))
        logits = self.fc2(h)
        return logits, h


def weight_moments(W):
    w = W.reshape(-1)
    mu = float(w.mean())
    var = float(w.var())
    std = math.sqrt(var + 1e-12)
    skew = float(np.mean(((w - mu) / std) ** 3))
    kurt = float(np.mean(((w - mu) / std) ** 4) - 3.0)
    return var, skew, kurt


def mean_pairwise_cosine(W):
    row_norms = np.linalg.norm(W, axis=1, keepdims=True) + 1e-12
    Z = W / row_norms
    C = Z @ Z.T
    n = C.shape[0]
    idx = np.triu_indices(n, k=1)
    return float(C[idx].mean()) if len(idx[0]) > 0 else np.nan


def effective_rank(W):
    s = np.linalg.svd(W, compute_uv=False)
    s = np.maximum(s, 1e-12)
    p = s / s.sum()
    entropy = -np.sum(p * np.log(p))
    return float(np.exp(entropy))


def brier_score_binary(probs, y_true):
    return float(np.mean((probs - y_true) ** 2))


@torch.no_grad()


def evaluate_model(model, loader, device):
    model.eval()
    total = 0
    correct = 0
    total_loss = 0.0
    all_probs = []
    all_y = []

    for xb, yb in loader:
        xb = xb.to(device)
        yb = yb.to(device)
        logits, _ = model(xb)
        loss = F.cross_entropy(logits, yb, reduction="sum")
        probs = torch.softmax(logits, dim=1)
        p1 = probs[:, 1]

        total_loss += float(loss.item())
        correct += int((logits.argmax(dim=1) == yb).sum().item())
        total += yb.size(0)

        all_probs.append(p1.detach().cpu().numpy())
        all_y.append(yb.detach().cpu().numpy())

    all_probs = np.concatenate(all_probs)
    all_y = np.concatenate(all_y)
    try:
        auc = float(roc_auc_score(all_y, all_probs))
    except Exception:
        auc = np.nan

    return {
        "nll": total_loss / max(total, 1),
        "acc": correct / max(total, 1),
        "brier": brier_score_binary(all_probs, all_y),
        "auc": auc,
        "mean_confidence": float(np.mean(np.maximum(all_probs, 1 - all_probs))),
    }


