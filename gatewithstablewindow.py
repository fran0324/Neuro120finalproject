def plot_history(tail_length, quantity, ylabel):
    plt.figure(figsize=(7, 4))
    for rule in rules:
        curves = [all_histories[(tail_length, rule, s)][["epoch", quantity]].copy() for s in seeds]
        min_len = min(len(c) for c in curves)
        epochs = curves[0]["epoch"].values[:min_len]
        vals = np.stack([c[quantity].values[:min_len] for c in curves], axis=0)
        mean = np.nanmean(vals, axis=0)
        std = np.nanstd(vals, axis=0)
        plt.plot(epochs, mean, label=rule)
        plt.fill_between(epochs, mean - std, mean + std, alpha=0.15)
    plt.xlabel("epoch")
    plt.ylabel(ylabel)
    plt.title(f"{ylabel} at tail length = {tail_length}")
    plt.legend()
    plt.tight_layout()
    plt.show()

